- (void) loadTableData {
    
    NSManagedObjectContext *context = [[self appDelegate] managedObjectContext];
    
    // Construct a fetch request
    NSFetchRequest *fetchRequest = [[NSFetchRequest alloc] init];
    NSEntityDescription *entity = [NSEntityDescription entityForName:@"Facts"inManagedObjectContext:context];
    
    [fetchRequest setEntity:entity];
    
    NSError *error = nil;
    facts = [context executeFetchRequest:fetchRequest error:&error];
    [self.tableView reloadData];
}

- (IBAction)factButton:(id)sender {
 
        f = [facts objectAtIndex:count];
        self.factShow.textLabel.numberOfLines = 0;
        self.factShow.textLabel.lineBreakMode = NSLineBreakByWordWrapping;
        self.factShow.textLabel.text = f.info;
        count++;
        if(count>2)
        {
            count=0;
        }
        [self.tableView reloadData];
    
    }
  
